j=(input(('digite algo')))
print('o tipo primitivo desse valor é',(type(j)))
print('ele é um numero?',j.isnumeric())
print('É alfabético?',j.isalpha())
print('estar em maiúculas?',j.isupper())
print('estar em minúsculas?',j.islower())
print('é um alfanumérico?',j.isalnum())
print('está capitalizada',j.istitle())



